package Chapter02;

public class Java_Variable {
    public static void main(String[] args) {
        // 声明变量
        String name;
        // 变量赋值
        name = "张三";

        System.out.println(name);

        String age = "20";
        System.out.println(age);

        String email = "xxxx@email.com";
        System.out.println(email);
    }
}
